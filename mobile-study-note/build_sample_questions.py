def main() -> None:
    questions = []
    serial = 1
    for group_name, items in GROUPS:
        if len(items) != 10:
            raise ValueError(f"{group_name} has {len(items)} questions")
        for question, choices, answer, explanation, section in items:
            if len(choices) != 3:
                raise ValueError(f"{group_name} question {serial} does not have 3 choices")
            questions.append(
                {
                    "id": f"hard-{serial:03d}",
                    "bank": "高難度予想100問",
                    "source": "教則ベース高難度予想",
                    "category": group_name,
                    "section": section,
                    "question": question,
                    "choices": choices,
                    "answer": answer,
                    "explanation": explanation,
                    "reference": section,
                }
            )
            serial += 1

    out = Path("sample_questions_100.json")
    out.write_text(json.dumps(questions, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"Wrote {len(questions)} questions to {out}")


if __name__ == "__main__":
    main()
