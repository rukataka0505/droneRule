def arrange_choices(correct: str, wrong1: str, wrong2: str, offset: int) -> tuple[list[str], int]:
    layouts = [
        ([correct, wrong1, wrong2], 0),
        ([wrong1, correct, wrong2], 1),
        ([wrong2, wrong1, correct], 2),
    ]
    return layouts[offset % 3]


def main() -> None:
    questions = []
    number = 1
    for group_index, (category, section, rows) in enumerate(GROUPS):
        for row_index, (question, correct, wrong1, wrong2, explanation) in enumerate(rows):
            choices, answer = arrange_choices(correct, wrong1, wrong2, group_index + row_index)
            questions.append(
                {
                    "id": f"sample-{number:03d}",
                    "bank": "分野別サンプル100問",
                    "source": "教則ベース追加サンプル",
                    "category": category,
                    "section": section,
                    "question": question,
                    "choices": choices,
                    "answer": answer,
                    "explanation": explanation,
                    "reference": section,
                }
            )
            number += 1
    if len(questions) != 100:
        raise SystemExit(f"expected 100 questions, got {len(questions)}")
    (ROOT / "sample_questions_100.json").write_text(
        json.dumps(questions, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )


